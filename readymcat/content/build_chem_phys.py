CP_CATEGORIES = {"4A", "4B", "4C", "4D", "4E", "5A", "5B", "5C", "5D", "5E"}
ALLOWED_DIFFICULTY = {"easy", "medium", "hard"}
ALLOWED_COGNITIVE = {"recall", "application"}


def load_taxonomy_categories() -> set:
    """Return the AAMC category IDs from taxonomy.json (repo root), or a fallback."""
    cur = HERE
    for _ in range(6):
        candidate = os.path.join(cur, "taxonomy.json")
        if os.path.exists(candidate):
            with open(candidate, "r", encoding="utf-8") as fh:
                tax = json.load(fh)
            return set(tax.get("aamc_categories", {})), candidate
        parent = os.path.dirname(cur)
        if parent == cur:
            break
        cur = parent
    return set(), None


def _check_options(loc, options, errors, n_min=4, n_max=4):
    if not isinstance(options, list):
        errors.append(f"[{loc}] options must be a list.")
        return
    if not (n_min <= len(options) <= n_max):
        errors.append(f"[{loc}] expected {n_min}-{n_max} options, found {len(options)}.")
    if len(set(options)) != len(options):
        errors.append(f"[{loc}] option texts are not unique.")
    for o in options:
        if not isinstance(o, str) or not o.strip():
            errors.append(f"[{loc}] an option is empty or not a string.")


def validate(items) -> int:
    errors: list = []
    tax_cats, tax_path = load_taxonomy_categories()
    valid_cats = tax_cats if tax_cats else CP_CATEGORIES

    seen_ids: set = set()
    per_category: collections.Counter = collections.Counter()
    per_subtopic: collections.Counter = collections.Counter()
    n_sub = 0

    for idx, it in enumerate(items):
        loc = it.get("id", f"index {idx}")
        for field in ("id", "section", "aamc_category", "subtopic", "stem",
                      "options", "correct_index", "explanation", "difficulty",
                      "cognitive_level", "source", "subquestions"):
            if field not in it:
                errors.append(f"[{loc}] missing required field '{field}'.")

        if it.get("section") != "C/P":
            errors.append(f"[{loc}] section must be 'C/P'.")

        if it.get("id") in seen_ids:
            errors.append(f"[{loc}] duplicate id.")
        seen_ids.add(it.get("id"))

        cat = it.get("aamc_category")
        per_category[cat] += 1
        if cat not in valid_cats:
            errors.append(f"[{loc}] aamc_category '{cat}' not in taxonomy.")
        if cat not in CP_CATEGORIES:
            errors.append(f"[{loc}] aamc_category '{cat}' is not a C/P category.")

        if not str(it.get("id", "")).startswith(f"cp-{cat}-"):
            errors.append(f"[{loc}] id should start with 'cp-{cat}-'.")

        per_subtopic[(cat, it.get("subtopic"))] += 1

        _check_options(loc, it.get("options", []), errors, 4, 4)
        ci = it.get("correct_index")
        if not isinstance(ci, int) or not (0 <= ci < len(it.get("options", []) or [1])):
            errors.append(f"[{loc}] correct_index out of range: {ci}.")

        if not str(it.get("explanation", "")).strip():
            errors.append(f"[{loc}] explanation is empty.")
        if it.get("difficulty") not in ALLOWED_DIFFICULTY:
            errors.append(f"[{loc}] difficulty '{it.get('difficulty')}' invalid.")
        if it.get("cognitive_level") not in ALLOWED_COGNITIVE:
            errors.append(f"[{loc}] cognitive_level '{it.get('cognitive_level')}' invalid.")

        src = it.get("source", {})
        if not isinstance(src, dict) or not all(src.get(k) for k in ("name", "url", "license")):
            errors.append(f"[{loc}] source must have name/url/license.")

        subs = it.get("subquestions", [])
        if not isinstance(subs, list) or not (2 <= len(subs) <= 3):
            errors.append(f"[{loc}] subquestions must be a ladder of 2-3 MCQs (found "
                          f"{len(subs) if isinstance(subs, list) else 'n/a'}).")
        else:
            for j, sq in enumerate(subs):
                sloc = f"{loc}#sub{j}"
                for field in ("stem", "options", "correct_index", "explanation"):
                    if field not in sq:
                        errors.append(f"[{sloc}] missing '{field}'.")
                _check_options(sloc, sq.get("options", []), errors, 2, 4)
                sci = sq.get("correct_index")
                if not isinstance(sci, int) or not (0 <= sci < len(sq.get("options", []) or [1])):
                    errors.append(f"[{sloc}] correct_index out of range: {sci}.")
                if not str(sq.get("stem", "")).strip():
                    errors.append(f"[{sloc}] stem is empty.")
                if not str(sq.get("explanation", "")).strip():
                    errors.append(f"[{sloc}] explanation is empty.")
                n_sub += 1

    missing = sorted(CP_CATEGORIES - set(per_category))
    if missing:
        errors.append(f"C/P categories with NO items: {missing}")

    # Report
    print(f"Taxonomy source: {tax_path or '(fallback embedded set)'}")
    print("\nCoverage report (items per C/P content category):")
    for cat in sorted(CP_CATEGORIES, key=lambda c: (int(c[:-1]), c[-1])):
        subs_in_cat = sorted({s for (c, s) in per_subtopic if c == cat})
        print(f"  {cat}: {per_category.get(cat, 0):>2} items across "
              f"{len(subs_in_cat)} subtopics")
        for s in subs_in_cat:
            print(f"        - {s} ({per_subtopic[(cat, s)]})")
    print(f"\nMain items: {len(items)} | Sub-questions: {n_sub} | "
          f"Total MCQs: {len(items) + n_sub}")
    print(f"C/P categories covered: {len(set(per_category) & CP_CATEGORIES)}/{len(CP_CATEGORIES)}")

    if errors:
        print(f"\nFAILED with {len(errors)} error(s):", file=sys.stderr)
        for e in errors:
            print(f"  - {e}", file=sys.stderr)
        return 1
    print("\nOK: all checks passed.")
    return 0


def main() -> int:
    items = build_items()
    with open(OUT, "w", encoding="utf-8") as fh:
        json.dump(items, fh, ensure_ascii=False, indent=2)
        fh.write("\n")
    # Re-load to prove the written file is valid JSON, then validate contents.
    with open(OUT, "r", encoding="utf-8") as fh:
        reloaded = json.load(fh)
    print(f"Wrote {OUT} ({len(reloaded)} items).\n")
    return validate(reloaded)


