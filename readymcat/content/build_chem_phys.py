# --- item constructors -----------------------------------------------------

def sub(stem: str, options: list, correct: int, explanation: str) -> dict:
    """A guiding sub-question (teach-on-miss ladder step). Also an MCQ."""
    return {"stem": stem, "options": options,
            "correct_index": correct, "explanation": explanation}


def item(id, cat, subtopic, stem, options, correct, explanation,
         difficulty, cognitive_level, source, subquestions) -> dict:
    return {
        "id": id,
        "section": "C/P",
        "aamc_category": cat,
        "subtopic": subtopic,
        "stem": stem,
        "options": options,
        "correct_index": correct,
        "explanation": explanation,
        "difficulty": difficulty,
        "cognitive_level": cognitive_level,
        "source": source,
        "subquestions": subquestions,
    }


