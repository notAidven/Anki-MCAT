def build():
    out = []
    for cat in ORDER:
        for i, it in enumerate(BANK.get(cat, []), 1):
            out.append({
                "id": f"fr-ps-{cat}-{i}",
                "section": "P/S",
                "aamc_category": cat,
                "subtopic": it["subtopic"],
                "answer_type": "free_response",
                "prompt": it["prompt"],
                "accepted_answers": it["accepted_answers"],
                "key_terms": it["key_terms"],
                "model_answer": it["model_answer"],
                "explanation": it["explanation"],
                "difficulty": it["difficulty"],
                "cognitive_level": it["cognitive_level"],
                "source": it["source"],
                "subquestions": it["subquestions"],
            })
    return out


def main():
    ap = argparse.ArgumentParser(description="Build free_response_psych_soc.json")
    ap.add_argument("--stdout", action="store_true",
                    help="print JSON to stdout instead of writing the file")
    args = ap.parse_args()

    data = build()
    text = json.dumps(data, indent=2, ensure_ascii=False) + "\n"
    if args.stdout:
        print(text, end="")
        return 0

    here = os.path.dirname(os.path.abspath(__file__))
    out_path = os.path.join(here, "free_response_psych_soc.json")
    with open(out_path, "w", encoding="utf-8") as fh:
        fh.write(text)

    by_cat: "dict[str, int]" = {}
    for rec in data:
        by_cat[rec["aamc_category"]] = by_cat.get(rec["aamc_category"], 0) + 1
    print(f"Wrote {len(data)} items to {out_path}\n")
    for cat in ORDER:
        print(f"  {cat:>3}  {CATEGORY_NAMES[cat]:44.44}: {by_cat.get(cat, 0)}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
